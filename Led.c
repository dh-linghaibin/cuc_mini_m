#include "Led.h"

#define LED_R   PC_ODR_ODR2
#define LED_G   PC_ODR_ODR4
#define LED_B   PC_ODR_ODR3 

#define LED_MODE PB_ODR_ODR1
/**********************************************函数定义***************************************************** 
* 函数名称: void LedInit(void) 
* 输入参数: void 
* 返回参数: void  
* 功    能: led初始化  
* 作    者: by lhb_steven
* 日    期: 2016/3/14
************************************************************************************************************/ 
void LedInit(void) { 
    PB_DDR_DDR1 = 1;//状态指示灯
    PB_CR1_C11 = 1;
    PB_CR2_C21 = 1;
    
    PC_DDR_DDR2 = 1;//电量 RGB
    PC_CR1_C12 = 1;
    PC_CR2_C22 = 1;
    
    PC_DDR_DDR3 = 1;
    PC_CR1_C13 = 1;
    PC_CR2_C23 = 1;
    
    PC_DDR_DDR4 = 1;
    PC_CR1_C14 = 1;
    PC_CR2_C22 = 1;
    LedSetPower(0);//关闭led
    LED_MODE = 1;
}
/***********************************************变量声明*****************************************************
* 功    能: 保存电量值  
* 作    者: by lhb_steven
* 日    期: 2016/3/17
************************************************************************************************************/ 
static u8 led_power_num = 0;
/**********************************************函数定义***************************************************** 
* 函数名称: void LedSetMode(u8 data) 
* 输入参数: u8 data 
* 返回参数: void  
* 功    能: 设置当前Led模式  
* 作    者: by lhb_steven
* 日    期: 2016/3/14
************************************************************************************************************/ 
void LedSetMode(u8 data) { 
    led_power_num = data;
}
/**********************************************函数定义***************************************************** 
* 函数名称: void LedSetPower(u8 cmd) 
* 输入参数: u8 cmd 
* 返回参数: void  
* 功    能: 设置电量状态  
* 作    者: by lhb_steven
* 日    期: 2016/3/14
************************************************************************************************************/ 
void LedSetPower(u8 cmd) { 
    switch( cmd ) {
        case 0:
        LED_R = 1;
        LED_G = 1;
        LED_B = 1;
        break;
        case 1:
        LED_R = 0;
        LED_G = 1;
        LED_B = 1;
        break;
        case 2:
        LED_R = 1;
        LED_G = 0;
        LED_B = 1;
        break;
        case 3:
        LED_R = 1;
        LED_G = 0;
        LED_B = 1;
        break;
        case 4:
        LED_R = 1;
        LED_G = 1;
        LED_B = 0;
        break;
        case 5:
        LED_R = 1;
        LED_G = 1;
        LED_B = 0;
        break;
        default:
        break;
    }
}
/***********************************************变量声明*****************************************************
* 功    能: 模式LED闪烁次数标志位  
* 作    者: by lhb_steven
* 日    期: 2016/3/17
************************************************************************************************************/ 
static u8 led_mode_flag = 2;
/**********************************************函数定义***************************************************** 
* 函数名称: void LedSetModeFlicker(u8 count) 
* 输入参数: u8 count 
* 返回参数: void  
* 功    能: 设置状态灯闪烁  count闪烁几次  
* 作    者: by lhb_steven
* 日    期: 2016/3/17
************************************************************************************************************/ 
void LedSetModeFlicker(u8 count) { 
    led_mode_flag = count;
}
/***********************************************变量声明*****************************************************
* 功    能: 警示灯闪烁次数状态标志位  
* 作    者: by lhb_steven
* 日    期: 2016/3/17
************************************************************************************************************/ 
static u8 led_power_flag = 0;
/**********************************************函数定义***************************************************** 
* 函数名称: void LedSetPowerFlag(void) 
* 输入参数: void 
* 返回参数: void  
* 功    能: 设置电池颜色显示  
* 作    者: by lhb_steven
* 日    期: 2016/3/17
************************************************************************************************************/ 
void LedSetPowerFlag(u8 data) { 
    led_power_flag = data;
}
/**********************************************函数定义***************************************************** 
* 函数名称: void LedTimeService(void) 
* 输入参数: void 
* 返回参数: void  
* 功    能: LED动画的服务函数  
* 作    者: by lhb_steven
* 日    期: 2016/3/16
************************************************************************************************************/ 
void LedTimeService(void) { 
    static u16 time_count = 0;
    if(time_count < 20000) {
        time_count++;
    } else {
        time_count = 0;
        if(led_mode_flag > 0) {
            static u8 bit = 0;            
            if(bit == 0) {
                bit = 1;
                led_mode_flag--;
            } else {
                bit = 0;
            }
            LED_MODE = bit;
        }
        if(led_power_flag > 0) {
            static u8 bit = 0;
            if(bit == 0) {
                bit = 1;
                led_power_flag--;
                LedSetPower(0);
            } else {
                bit = 0;
                LedSetPower(led_power_num);
            }
        }
    }
}










